Design=cbind(c(0.1,0.2,0.4,0.5),c(rep(0.7,4)))
res_lagged_K=list()
for(j in 1:nrow(Design)){
  K=Design[j,1]
  mP<-m_th<-log(Design[j,2]/(1-Design[j,2]))
  res_lagged_K[[j]]<-elo_lag(n=1000,m=200,games=1500,reps=500,K=K,mP=mP,m_th=m_th,LH=500)
}
