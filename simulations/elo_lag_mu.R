################################################################################
# Dependencies
################################################################################
source('elo_functions.R')

################################################################################
# simulation
################################################################################
Design=cbind(c(0.3,0.3,0.3,0.3),c(0.5,0.6,0.8,0.9))
res_lagged_mu=list()
for(j in 1:nrow(Design)){
  K=Design[j,1]
  mP<-m_th<-log(Design[j,2]/(1-Design[j,2]))
  res_lagged_mu[[j]]<-elo_lag(n=1000,m=200,games=1500,reps=2,K=K,mP=mP,m_th=m_th,LH=500)
}

################################################################################
# Saving the results as a list of lists
################################################################################
save(res_lagged_mu,file="output/ResultsEloLag_mu.RData")